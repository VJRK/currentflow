def assemble_level(index):
    Barrier.instances = []
    x = 0
    y = 0
    for row in all_levels[index]:
        for char in row:
            if char == "W":
                Barrier("Wall", x, y)
            elif char == "T":
                Barrier("Wall", x, y, height=.55)
            elif char == "B":
                Barrier("Wall", x, y+.5, height=.55)
            elif char == "R":
                Barrier("RampR", x, y)
            elif char == "L":
                Barrier("RampL", x, y)
            elif char == "Ṙ":
                Barrier("RampR", x, y)
                Barrier("Point", x+1.001, y+.01, height=.0001, width=.0001)
            elif char == "Ḷ":
                Barrier("RampL", x, y)
                Barrier("Point", x, y+.01, height=.0001, width=.0001)
            elif char == "b":
                Barrier("Wall", x, y+.7, height=.35)
            elif char == "l":
                Barrier("Wall", x, y, width=.55)
                Barrier("RampL", x+.5, y)
            elif char == "r":
                Barrier("Wall", x, y+.7, height=.35)
                Barrier("RampR", x, y)

            elif char == "F":
                flow = Flow(x, y, .65, .85)
            elif char == "C":
                current = Current(x, y, .65, .85)
            elif char == " ":
                continue
            x += 1
        y += 1
        x = 0
    return flow, current
